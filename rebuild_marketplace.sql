CREATE TABLE public.vendor_categories (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vendors_id uuid REFERENCES public.vendors(id) ON DELETE CASCADE,
    categories_id uuid REFERENCES public.categories(id) ON DELETE CASCADE
);

CREATE TABLE public.employees (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    vendor_id uuid REFERENCES public.vendors(id) ON DELETE CASCADE,
    name character varying(255) NOT NULL,
    email character varying(255),
    photo uuid,
    bio text,
    timezone character varying(255) DEFAULT 'UTC',
    status character varying(50) DEFAULT 'active',
    sort_order integer DEFAULT 0
);

CREATE TABLE public.employee_services (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    employee_id uuid REFERENCES public.employees(id) ON DELETE CASCADE,
    name character varying(255) NOT NULL,
    description text,
    price decimal(10,2) NOT NULL,
    duration_minutes integer NOT NULL DEFAULT 30,
    is_active boolean DEFAULT true,
    sort_order integer DEFAULT 0
);

CREATE TABLE public.employee_schedules (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    employee_id uuid REFERENCES public.employees(id) ON DELETE CASCADE,
    day_of_week integer NOT NULL, -- 0-6
    start_time time without time zone NOT NULL,
    end_time time without time zone NOT NULL,
    is_closed boolean DEFAULT false,
    sort_order integer DEFAULT 0
);

CREATE TABLE public.working_hours (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    vendor_id uuid REFERENCES public.vendors(id) ON DELETE CASCADE,
    day_of_week integer NOT NULL,
    open_time time without time zone NOT NULL,
    close_time time without time zone NOT NULL,
    is_closed boolean DEFAULT false
);

CREATE TABLE public.bookings (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    employee_id uuid REFERENCES public.employees(id),
    vendor_id uuid REFERENCES public.vendors(id),
    employee_service_id uuid REFERENCES public.employee_services(id),
    booker_email character varying(255) NOT NULL,
    booker_name character varying(255) NOT NULL,
    start_datetime timestamp with time zone NOT NULL,
    end_datetime timestamp with time zone NOT NULL,
    status character varying(50) DEFAULT 'pending', -- pending, confirmed, cancelled
    amount decimal(10,2),
    notes text,
    created_at timestamp with time zone DEFAULT now()
);

CREATE TABLE public.reviews (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    vendor_id uuid REFERENCES public.vendors(id),
    customer_name character varying(255),
    customer_email character varying(255),
    rating integer CHECK (rating >= 1 AND rating <= 5),
    comment text,
    is_verified boolean DEFAULT false,
    status character varying(50) DEFAULT 'active',
    created_at timestamp with time zone DEFAULT now()
);

